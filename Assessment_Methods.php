                    <h1> Assessment Method</h1>
                    <p> There are a number of assessment methods, each of which is appropriate for different assessment
                        purposes. Assessment methods provide feedback throughout learning process, measure learning of
                        students at the end of the course, provide standardization of program and institute, used for
                        placement purpose, etc. However, the common goal of all assessment methods is to evaluate and
                        improve the teaching learning process. Our department is currently focusing on student feedback
                        mechanism, ongoing performance monitoring, evaluation system, role of parents, alumni, and
                        industry as well as the exit survey for graduating students.

                    </p>
                    <hr>
                    <h2> Student feedback system</h2>
                    <p> Students Feedback Form in 3-section covers the important aspects of teaching strategy, course
                        contents and other constructive suggestions by the students for further improvements. The
                        students are asked to rate the teacher of their concerned course on the basis of ten parameters
                        at a five-point scale. Also, the students give feedback related to the course contents and the
                        teaching strategy for the same course. Apart from these aspects, the students are required to
                        give their comments for the improvements of teachers performance, if any. A meeting of
                        students, teacher and head is called at the beginning of the session to tell the students the
                        mission and objectives of the organization as well as the feedback system at the time of
                        orientation program for new students. The assessment and research committee demonstrates the
                        students about the purpose of filling feedback system at the time of filling feedback form (i.e.
                        time of comprehensive viva). They are convinced that the identity of students will be
                        confidential and even the feedback form does not contain any kind of identity. The top 15-20
                        students are short listed by the committee to fill up the feedback form for each subject taught
                        by the teacher. The students feedback forms are collected and punched into a Course Assessment
                        File for rigorous analysis.
                    </p>
                    <hr>
                    <h2> Ongoing performance monitoring</h2>
                    <p> Student achievement on quizzes, seminars, tutorials, case studies, assignments, laboratory
                        experiments and class and laboratory attendance are regularly monitored to determine how well
                        students are responding to the instruction. With above assessment attributes, teachers have the
                        opportunity to ask yourself whether students are capable of what they should be as a result of
                        participating in your class up to that point and meeting the course objectives. Students are
                        advised to maintain the assignments and experiments copy, which are to be submitted at time of
                        comprehensive viva. Assessment and research committee collects results of quizzes and/ or other
                        events organized by the teachers, assignment and experiment copy, and laboratory and class room
                        attendance records.

                    </p>
                    <hr>
                    <h2> Student evaluation system</h2>
                    <p> Students are evaluated through conducting Internal and External examination. In internal
                        evaluation, students are measured through mid-term exams, project development, classroom
                        performance, laboratory exam and special measures used (if any) by the teachers. External
                        evaluation is performed through end semester examination and comprehensive viva. The results of
                        these evaluations are prepared carefully enough so that the answers could help you a clear
                        picture of whether and how students are meeting the course objectives. The results of end
                        semester examination and comprehensive viva are collected for individual courses for further
                        assessment purpose.
                    </p>
                    <hr>
                    <h2> Parent teacher meet</h2>
                    <p> To develop the communication that will allow parents and teachers to make plans, set goals,
                        solve problems, and establish the relationship that they need in order to have a good
                        partnership, a Parent-Teacher Meeting is required. A parent teacher meet helps to communicate to
                        parents the areas their children are excelling in and to give them specific ideas of how to
                        improve upon their childs performance. Parent-Teacher meeting should be used as a platform to
                        make a lasting bond with the parent to increase the likelihood of academic success for their
                        child. A kit was prepared which included an information brochure, a pen and a writing pad to
                        write suggestions on Parent-Teacher Meeting. Also a feedback form was designed in order to take
                        feedback of parents on the teaching-learning process at our institute. Report of the meet
                        assists for teaching learning assessment.

                    </p>
                    <hr>
                    <h2> Alumni meet</h2>
                    <p> The purpose of Alumni Meet is to strengthen the relationships between alumni and the Institute.
                        It gives a stronger industry-specific orientation and enhances the learning skills of students.
                        The event brings together a wealth of talented and capable professionals who share their
                        expertise and experience, and brainstorm on the prospective avenues for further benefits for the
                        students. The Alumni Meet also provides an opportunity to the Institute to acknowledge the
                        contributions and achievements of its alumni. The alumni meet has been formed with a prime
                        objective of bridging the gap between the Institute and its alumni who are placed in different
                        corners of the globe. This would help us to develop and maintain a mutually beneficial
                        relationship and thereby to extend support and contribute to the life of the Institute and its
                        various activities


                    </p>
                    <hr>
                    <h2> Industry meet</h2>
                    <p> The Department has developed a strong relation with Industry by providing them good man power in
                        a form of post graduate students. Every year students of this institute joins the various
                        software and research & development (R&D) industry. Experts from industry share their experience
                        with the students of this department through classroom lectures as well as special training
                        sessions. Now in order to get the feedback from industry we required to start an industry meet
                        with the various software and R&D industry. Also the past students of the department have
                        reached to the level of Lead Engineer / Senior Managers / Head of Department in their respective
                        organizations. The past students and experts from the industry will provide us the feedback
                        regarding our curriculum and the process, to cope-up with the frequent changes occurs in the
                        industry. This will create better professionals which help in achieving the goals of department
                        as well as the industry.
                    </p>
                    <hr>
                    <h2> Exit survey for graduating students</h2>
                    <p> Graduating seniors are required, as part of their application to graduate, to complete an exit
                        survey that asks the student to rank the importance of each program objective and outcome. For
                        each outcome, we determine how well the program met the particular outcome in his or her
                        individual case. For each objective, how well the program prepared the student to achieve the
                        particular objective in the years following his or her graduation. The social atmosphere and
                        extra curriculum activities required for the learning of students are investigated for later
                        improvements. Thus, the students provide feedback of overall academic experiences received from
                        the department and its society. The results of exit survey are summarized and included in the
                        course assessment file.
                    </p>
