                    <h1>National Service Scheme (NSS)</h1>
                    <p>
                        SCSIT has an NSS unit. Students from all the streams are members of NSS. The
                        Motto of NSS "Not Me But You", reflects the essence of democratic living and
                        upholds the need for self-less service. NSS helps the students to develop
                        appreciation to other person's point of view and also show consideration to
                        other living beings. NSS focuses on welfare of an individual, which is
                        ultimately dependent on the welfare of the society on the whole and therefore,
                        the NSS volunteers at SCSIT strive for the well-being of the society.
                    </p><br>
                    <p>
                        NSS programme aims to inculcate social welfare in students, and to provide
                        service to society without bias. NSS volunteers at SCSIT work to ensure that
                        everyone who is needy gets help to enhance their standard of living and lead a
                        life of dignity. In doing so, volunteers learn from people in villages how to
                        lead a good life despite a scarcity of resources. NSS also provides help in
                        natural and man-made disasters by providing food, clothing and first aid to
                        the disaster victims.
                    </p><br>
                    <p>
                        Camps are held annually, funded by the government of India, and are usually located in a rural
                        village or a city suburb. Volunteers may be involved in such activities as:
                    </p>
                    <ul style="margin-left: 20px;">
                        <li>Cleaning</li>
                        <li>Afforestation</li>
                        <li>
                            Stage shows or a procession creating awareness of such issues as social
                            problems, education and cleanliness
                        </li>
                        <li>Awareness Rallies</li>
                        <li>Inviting doctors for health camps</li>
                    </ul><br>
                    <p>
                        There are no predefined or preassigned tasks; it is left up to the volunteers to provide service
                        in any way that is feasible. Students of SCSIT conducted the following activities of NSS:
                    </p>
                    <ul style="margin-left: 20px;">
                        <li>Campus cleaning</li>
                        <li>Plantation</li>
                        <li>Educating rural background students</li>
                        <li>
                            Awareness in villages for Linking Aadhaar card with LPG, Bank Account etc.
                        </li>
                        <li>Awareness Rally for Vote</li>
                        <li>Cleaning village</li>
                        <li>Woman Empowerment</li>
                        <li>Yoga activities</li>
                    </ul>
                    
