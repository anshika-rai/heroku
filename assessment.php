                    <h1>Assessment</h1>
                    <p>
                        The increasing society of education in recent years forces to maintain the quality of teaching
                        learning in higher education. Also the competition in the market is approaching to receive
                        students with a higher quality of learning. Teaching is measured by learning outcomes. Teachers
                        need to be learner of the subject for the effectiveness of their teaching. Learning emphasizes
                        on deep understanding of subjects and skills in students. Quality of teaching learning can be
                        improved through monitoring, evaluation and assessment. Monitoring is the gathering and
                        recording of data about teachers, students, courses, etc. regularly at department level.
                        Evaluation employs data gathered during monitoring process for system accountability. Assessment
                        is an ongoing process of monitoring and evaluating the performance aimed at understanding and
                        improving learning process. Assessment is a process of systematically gathering, analyzing and
                        interpreting evidence to determine the performance according to the expectations and standards.
                        Assessment data can be obtained from various stakeholders (e.g. students, teachers, parents,
                        alumni, industry, evaluation system, etc.) of the department. Assessment data gathered from
                        various stakeholders can be used to promote continuous improvement efforts, which may lead to
                        institutional effectiveness. The assessment process involves the collection and analysis of data
                        regarding teaching strategy and students learning in an individual course. Analyzed data can be
                        used to inform future teaching and learning decisions.
