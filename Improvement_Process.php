                    <h1> Improvement Process</h1>
                    <p> The students feedback forms are collected and scrutinized course wise for individual teacher.
                        Along with filled feedback forms, other documents and reports of various assessment methods
                        (ongoing performance monitoring, student evaluation system, industry meet, parent meet, alumni
                        meet, and exit survey) are gathered for assessment. A Course Assessment File (CAF) comprising of
                        above documents is prepared for individual course with respective individual teacher. The CAF
                        are grouped according to the programmes. The members of assessment and research committee
                        analyze and monitor each CAF rigorously. The review and monitoring process mainly emphasizes on
                        Subject Learning Objectives (SLO), Comments related to the course contents and effectiveness of
                        Teaching-Learning Strategies used to teach the subjects included in he specified academic
                        session by the respective faculty members. A report of analysis for each CAF is prepared. A
                        summary of all CAF is made and submitted to the Head of the Department and the individual
                        feedback report is sent to the concerned teacher. Finally, Head of the department calls an
                        informal meeting of teachers with summary submitted by the assessment and research committee and
                        discusses the issues where the improvements are needed in overall. Assessment Progress Report
                        (APR) is a feedback mechanism that we prepare which allows us to integrate the results of the
                        various assessment methods and identify appropriate actions to improve the program. An APR is
                        prepared by the assessment and research committee, which covers the summary and detailed report
                        of assessment done. It also includes the improvements found as compared to previous session.
                        Detailed report includes the report of individual courses taught by the teacher, general
                        comments, best practices used by the teachers and report of visiting faculty involved if any.
                        Final APR is submitted to Head of the Department and circulated to the members of various other
                        committees. Assessment and research committee is connected with other internal committees of the
                        department such as administrative committee, academic planning committee, infrastructure
                        development committee, library committee, placement committee, result committee etc. Each of
                        these committees consists of an advisor, group of faculty members and student representatives
                        (somewhere). These committees have many members in common with each other, which helps to
                        coordinate their actions.
                    </p><br>
                    <p> Finally, assessment and research committee organize a formal workshop on quality assurance,
                        assessment and accreditation in which members of internal committees of the department
                        participate and share their views and opinions. The experts are invited for talk and assessment
                        of the workshop. Assessment committee presents his APR and each faculty presents his/ her course
                        assessment in a prescribed proforma regarding the experiences found in teaching learning of an
                        aforesaid academic session. Head of the department guides us for further improvement in teaching
                        learning. The outcomes of workshop assist to various committees for improvements and possible
                        courses of actions in teaching learning at all levels of the department. Along with these
                        outcomes assessment committee and Head of the Department calls regular meetings for teaching
                        learning improvement.
                    </p>

